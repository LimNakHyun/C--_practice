long long int fact(long long int n) { 

/***************************************************************************** 
*                                                                            * 
* 재귀적으로 계승을 계산.                                                    * 
*                                                                            * 
*****************************************************************************/ 

if (n < 0)
    return 0;
else if (n == 0)
    return 1;
else if (n == 1)
    return 1;
else
    return n * fact(n - 1);
}

int main()
{
    long long int n;
    cout << "n팩토리얼: ";
    cin >> n;
    cout << fact(n) << endl;
}