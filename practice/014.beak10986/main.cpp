#include <iostream>

using namespace std;

int main()
{
    int n, m;
    cin >> n >> m;

    int *a;
    a = new int[n + 1]();

    for(int i = 1; i <= n; i ++) {
        int temp;
        cin >> temp;
        a[i] = a[i - 1] + temp;
    }

    for(int i = 0; i < n + 1; i++) {
        cout << a[i] << " ";
    }
}